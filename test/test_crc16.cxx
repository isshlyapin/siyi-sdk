#include <vector>

#include <gtest/gtest.h>
#include <siyi-sdk/crc16.hxx>

using namespace siyi::crc;

// Все известные команды из документации ZR10 User Manual v1.7
TEST(crc16, DefaultTests) {
  // 55 66 01 00 00 00 00 19 5D 57
  std::vector<uint8_t> data{0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x19};
  EXPECT_EQ(crc16(data), 0x575D);

  // 55 66 01 01 00 00 00 05 01 8d 64
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x05, 0x01};
  EXPECT_EQ(crc16(data), 0x648D);

  // 55 66 01 01 00 00 00 05 FF 5c 6a
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x05, 0xFF};
  EXPECT_EQ(crc16(data), 0x6A5C);

  // 55 66 01 02 00 10 00 0f 04 05 6b 15
  data = {0x55, 0x66, 0x01, 0x02, 0x00, 0x10, 0x00, 0x0f, 0x04, 0x05};
  EXPECT_EQ(crc16(data), 0x156B);

  // 55 66 01 00 00 00 00 16 B2 A6
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x16};
  EXPECT_EQ(crc16(data), 0xA6B2);

  // 55 66 01 00 00 00 00 18 7C 47
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x18};
  EXPECT_EQ(crc16(data), 0x477C);

  // 55 66 01 01 00 00 00 06 01 de 31
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x06, 0x01};
  EXPECT_EQ(crc16(data), 0x31DE);

  // 55 66 01 01 00 00 00 06 ff 0f 3f
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x06, 0xff};
  EXPECT_EQ(crc16(data), 0x3F0F);

  // 55 66 01 01 00 00 00 0c 00 34 ce
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x00};
  EXPECT_EQ(crc16(data), 0xCE34);

  // 55 66 01 01 00 00 00 0c 02 76 ee
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x02};
  EXPECT_EQ(crc16(data), 0xEE76);

  // 55 66 01 02 00 00 00 07 64 64 3d cf
  data = {0x55, 0x66, 0x01, 0x02, 0x00, 0x00, 0x00, 0x07, 0x64, 0x64};
  EXPECT_EQ(crc16(data), 0xCF3D);

  // 55 66 01 01 00 00 00 08 01 d1 12
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x08, 0x01};
  EXPECT_EQ(crc16(data), 0x12D1);

  // 55 66 01 00 00 00 00 0a 0f 75
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x0a};
  EXPECT_EQ(crc16(data), 0x750F);

  // 55 66 01 01 00 00 00 04 01 bc 57
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x04, 0x01};
  EXPECT_EQ(crc16(data), 0x57BC);

  // 55 66 01 00 00 00 00 02 07 f4
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x02};
  EXPECT_EQ(crc16(data), 0xF407);

  // 55 66 01 00 00 00 00 01 64 c4
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x01};
  EXPECT_EQ(crc16(data), 0xC464);

  // 55 66 01 01 00 00 00 0c 03 57 fe
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x03};
  EXPECT_EQ(crc16(data), 0xFE57);

  // 55 66 01 01 00 00 00 0c 04 b0 8e
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x04};
  EXPECT_EQ(crc16(data), 0x8EB0);

  // 55 66 01 01 00 00 00 0c 05 91 9e
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x05};
  EXPECT_EQ(crc16(data), 0x9E91);

  // 55 66 01 00 00 00 00 0d e8 05
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x0d};
  EXPECT_EQ(crc16(data), 0x05E8);

  // 55 66 01 04 00 00 00 0e 00 00 ff a6 3b 11
  data = {0x55, 0x66, 0x01, 0x04, 0x00, 0x00, 0x00, 0x0e, 0x00, 0x00, 0xff, 0xa6};
  EXPECT_EQ(crc16(data), 0x113B);

  // 55 66 01 01 00 00 00 0c 06 f2 ae
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x06};
  EXPECT_EQ(crc16(data), 0xAEF2);

  // 55 66 01 01 00 00 00 0c 07 d3 be
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x07};
  EXPECT_EQ(crc16(data), 0xBED3);

  // 55 66 01 01 00 00 00 0c 08 3c 4f
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x0c, 0x08};
  EXPECT_EQ(crc16(data), 0x4F3C);

  // 55 66 01 01 00 00 00 20 00 BF 8D
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x20, 0x00};
  EXPECT_EQ(crc16(data), 0x8DBF);

  // 55 66 01 09 00 00 00 21 01 02 00 05 d0 02 dc 05 00 58 45
  data = {0x55, 0x66, 0x01, 0x09, 0x00, 0x00, 0x00, 0x21, 0x01, 0x02, 0x00, 0x05, 0xd0, 0x02, 0xdc, 0x05, 0x00};
  EXPECT_EQ(crc16(data), 0x4558);

  // 55 66 01 09 00 00 00 21 01 02 80 07 38 04 d0 07 00 5a 68
  data = {0x55, 0x66, 0x01, 0x09, 0x00, 0x00, 0x00, 0x21, 0x01, 0x02, 0x80, 0x07, 0x38, 0x04, 0xd0, 0x07, 0x00};
  EXPECT_EQ(crc16(data), 0x685A);

  // 55 66 01 09 00 00 00 21 00 02 00 0a a0 05 98 3a 00 15 f3
  data = {0x55, 0x66, 0x01, 0x09, 0x00, 0x00, 0x00, 0x21, 0x00, 0x02, 0x00, 0x0a, 0xa0, 0x05, 0x98, 0x3a, 0x00};
  EXPECT_EQ(crc16(data), 0xf315);

  // 55 66 01 09 00 00 00 21 00 02 00 0f 70 08 98 3a 00 70 be
  data = {0x55, 0x66, 0x01, 0x09, 0x00, 0x00, 0x00, 0x21, 0x00, 0x02, 0x00, 0x0f, 0x70, 0x08, 0x98, 0x3a, 0x00};
  EXPECT_EQ(crc16(data), 0xbe70);

  // 55 66 01 00 00 00 00 10 74 c6
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x10};
  EXPECT_EQ(crc16(data), 0xc674);

  // 55 66 01 01 00 00 00 11 01 3a ab
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x11, 0x01};
  EXPECT_EQ(crc16(data), 0xab3a);

  // 55 66 01 05 00 00 00 12 00 00 00 00 01 a8 2f
  data = {0x55, 0x66, 0x01, 0x05, 0x00, 0x00, 0x00, 0x12, 0x00, 0x00, 0x00, 0x00, 0x01};
  EXPECT_EQ(crc16(data), 0x2FA8);

  // 55 66 01 00 00 00 00 1A 3e 67
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x1A};
  EXPECT_EQ(crc16(data), 0x673E);

  // 55 66 01 01 00 00 00 1B 03 b3 64
  data = {0x55, 0x66, 0x01, 0x01, 0x00, 0x00, 0x00, 0x1B, 0x03};
  EXPECT_EQ(crc16(data), 0x64B3);

  // 55 66 01 00 00 00 00 15 D1 96
  data = {0x55, 0x66, 0x01, 0x00, 0x00, 0x00, 0x00, 0x15};
  EXPECT_EQ(crc16(data), 0x96D1);
}